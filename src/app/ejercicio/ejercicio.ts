/*
* Esta clase contiene toda la infoirmacion importante para el ejercicio
*/
export class Ejercicio{

    /**
     * El nombre de un ejercicio
     */
    nombre: string;

    /**
     * La descripcion de un ejercicio
     */
    descripcion: string;

    /**
     * El identificador unico de un numero
     */
    id: number;

    /**
     * La ruta del input de un ejercicio
    */
    inputt: string;

    /** 
    * LA ruta del output de un ejercicio
    */
    outputt: string;

    /** 
     * El puntaje que ofrece un ejercicio
     */
    puntaje: number;

    /**
     * El nivel de dificultad de un ejercicio
     */
    nivel: number;



}